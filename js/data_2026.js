function buildGroupsFromTeamMap(teamMap) {
    const groups = {};

    Object.values(teamMap).forEach(({ name, group}) => {
        if (!groups[group]) {
            groups[group] = [];
        }
        groups[group].push(name);
    });

    return groups;
}